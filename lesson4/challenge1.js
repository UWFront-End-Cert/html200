function numberDoubler() {
    let input = prompt('Enter a number');
    let result = 0;
    result = input * input;
    
    alert(result);
}

function dualInput() {
    let firstInput = prompt('Enter your first number');
    let secondInput = prompt('Enter your second number');
    let dualInputResult = 0;
    
    dualInputResult = firstInput * secondInput;

    alert(dualInputResult);
}