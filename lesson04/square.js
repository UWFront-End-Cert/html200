function squareNum() {
  let input = prompt('Enter a number to find its square root.');

  let result = 0;

  result = Number(input) * Number(input);

  alert(result);
}

function multNum() {
  let x = prompt('Enter your first number.')
  let y = prompt('Enter your second number.')

  let result = 0;

  result = Number(x) * Number(y);

  alert(result);
}
