// IF/ELSE STATEMENTS
// Checks something, if true then do it, if not, do something else
// Generally don't use more than 2 or 3 if/else statements. At that point use a switch statement


let x = 'hi there';
let y = 'james';

if (15 / 5 === 5) {
    console.log(x);
} else if (x === 'james') {
    console.log(y);
} else if (y === 'james') {
    console.log('finally');
}