// ALERTS AND PROMPTS
// Prompts take two arguments - prompt()
    // The message you want to display
    // A default value if the user clicks "cancel" on the prompt
    // The output of the prompt can be stored in a variable

// Alerts take one argument - alert() - The message you want to display


// Ask for input using a dialog box
const dogBreed = prompt('What type of dog do you have?', 'none');
// Display input in a dialog box
alert(dogBreed);
// Ask for input with no default value
const dogAge = prompt('How old is your dog?');
// Display input in a dialog box
alert(dogAge);