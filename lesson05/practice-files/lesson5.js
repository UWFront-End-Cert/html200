// CONTROL FLOW - SWITCH STATEMENT

const dogBreed = 'Toller';

switch (dogBreed) {
    case 'Toller':
        console.log('Your dog is a Canadian breed');
        break;
    case 'Schnauzer':
        console.log('Your dog is a German breed');
        break;
    default: console.log('We do not have info on that breed');
        break;
}