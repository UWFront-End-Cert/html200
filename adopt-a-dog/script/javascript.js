function ContactUsForm() {
    const variables = ['FirstName', 'LastName', 'EmailId','Comments'];
    const form = document.getElementById('contactUs-form');

    webpageOnLoad();

    for (let i = 0; i < variables.length; i++) {
        const label = document.createElement('label');
        label.textContent = variables[i];
        label.setAttribute ('for', variables[i]);

        form.appendChild(label);

        if (variables[i] === 'Comments') {
            var textarea = document.createElement("TEXTAREA");
            textarea.setAttribute('id', variables[i]);
            form.appendChild(textarea);
        }  else {
            var input = document.createElement("input");
            input.setAttribute('id', variables[i]);
            //if (variables[i] === 'EmailId') {
            //    input.setAttribute ('type', 'email');
            //}
            form.appendChild(input);
        }
    }

    const submitButton= document.createElement('button');
    submitButton.setAttribute('type','submit');
    submitButton.setAttribute('class', 'checkout-button');
    submitButton.textContent = 'Submit';

    form.appendChild(submitButton);

    try {
        form.addEventListener('submit', function() {
            var error = 0;
            for (i = 0; i < variables.length ; i++) {
              const string = document.getElementById(variables[i]).value;
              console.log(variables[i] + " values is = " + string);
              if( string.trim() === "") {
                 alert ('Kindly enter all data in the form.');
                 error = 1;
                 document.getElementById(variables[i]).focus();
                 break;
              }
              else if (variables[i] === "EmailId" &&  invalidEmail(string)) {
                  alert ('Kindly enter correct email address...!');
                  error = 1;
                  document.getElementById(variables[i]).focus();
                  break;
              }
            }
            if (error === 0) {
              alert ("Your submission has been received");
            }
      });

      function invalidEmail(word) {
        var mailformat = /^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,3})+$/;
        if(word.match(mailformat)) {
          return false;
        }
        else {
          return true;
        }
      }
  }
  catch (err) {
    console.log("Error Handled");
  }
}
